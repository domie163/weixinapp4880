				<image :style='{"width":"80rpx","borderRadius":"100%","objectFit":"cover","display":"block","height":"80rpx"}' class="avator" v-if="ruleForm.shangjiazhaopian" :src="baseUrl+ruleForm.shangjiazhaopian.split(',')[0]" mode="aspectFill"></image>
