<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				huifubiaoti: '',
				huifuneirong: '',
				huifushijian: '',
				yanyuanzhanghao: '',
				nicheng: '',
				touxiang: '',
				shangjiazhanghao: '',
				shangjianicheng: '',
				},
				// 登陆用户信息
				user: {},
                ro:{
                   huifubiaoti : false,
                   huifuneirong : false,
                   huifushijian : false,
                   yanyuanzhanghao : false,
                   nicheng : false,
                   touxiang : false,
                   shangjiazhanghao : false,
                   shangjianicheng : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.huifushijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yanyuanzhanghao = this.user.yanyuanzhanghao
			this.ro.yanyuanzhanghao = true;
			this.ruleForm.nicheng = this.user.nicheng
			this.ro.nicheng = true;



			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xiaoxihuifu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='huifubiaoti'){
					this.ruleForm.huifubiaoti = obj[o];
					this.ro.huifubiaoti = true;
					continue;
					}
					if(o=='huifuneirong'){
					this.ruleForm.huifuneirong = obj[o];
					this.ro.huifuneirong = true;
					continue;
					}
					if(o=='huifushijian'){
					this.ruleForm.huifushijian = obj[o];
					this.ro.huifushijian = true;
					continue;
					}
					if(o=='yanyuanzhanghao'){
					this.ruleForm.yanyuanzhanghao = obj[o];
					this.ro.yanyuanzhanghao = true;
					continue;
					}
					if(o=='nicheng'){
					this.ruleForm.nicheng = obj[o];
					this.ro.nicheng = true;
					continue;
					}
					if(o=='touxiang'){
					this.ruleForm.touxiang = obj[o].split(",")[0];
					this.ro.touxiang = true;
					continue;
					}
					if(o=='shangjiazhanghao'){
					this.ruleForm.shangjiazhanghao = obj[o];
					this.ro.shangjiazhanghao = true;
					continue;
					}
					if(o=='shangjianicheng'){
					this.ruleForm.shangjianicheng = obj[o];
					this.ro.shangjianicheng = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
            huifuneirongChange(e) {
                this.ruleForm.huifuneirong = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数


			// 日长控件选择日期时间
			huifushijianConfirm(val) {
				console.log(val)
				this.ruleForm.huifushijian = val.result;
				this.$forceUpdate();
			},


			touxiangTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.touxiang = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {

















//跨表计算判断
				var obj;
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`xiaoxihuifu`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`xiaoxihuifu`, this.ruleForm);
						}else{
							await this.$api.add(`xiaoxihuifu`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`xiaoxihuifu`, this.ruleForm);
					}else{
						await this.$api.add(`xiaoxihuifu`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

