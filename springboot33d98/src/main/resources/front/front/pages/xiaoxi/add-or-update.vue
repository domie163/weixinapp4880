<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				tonggaobiaoti: '',
				tongzhineirong: '',
				tongzhileixing: '',
				tongzhishijian: '',
				yanyuanzhanghao: '',
				nicheng: '',
				lianxidianhua: '',
				shangjiazhanghao: '',
				shangjianicheng: '',
				lianxifangshi: '',
				shangjiazhaopian: '',
				},
				tongzhileixingOptions: [],
				tongzhileixingIndex: 0,
				yanyuanzhanghaoOptions: [],
				yanyuanzhanghaoIndex: 0,
				// 登陆用户信息
				user: {},
                ro:{
                   tonggaobiaoti : false,
                   tongzhineirong : false,
                   tongzhileixing : false,
                   tongzhishijian : false,
                   yanyuanzhanghao : false,
                   nicheng : false,
                   lianxidianhua : false,
                   shangjiazhanghao : false,
                   shangjianicheng : false,
                   lianxifangshi : false,
                   shangjiazhaopian : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.tongzhishijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.shangjiazhanghao = this.user.shangjiazhanghao
			this.ro.shangjiazhanghao = true;
			this.ruleForm.shangjianicheng = this.user.shangjianicheng
			this.ro.shangjianicheng = true;
			this.ruleForm.lianxifangshi = this.user.lianxifangshi
			this.ro.lianxifangshi = true;


			// 自定义下拉框值
			this.tongzhileixingOptions = "已入选,已完成".split(',')
			// 下2
			res = await this.$api.option(`xiaoyanyuan`,`yanyuanzhanghao`,{});
			this.yanyuanzhanghaoOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xiaoxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='tonggaobiaoti'){
					this.ruleForm.tonggaobiaoti = obj[o];
					this.ro.tonggaobiaoti = true;
					continue;
					}
					if(o=='tongzhineirong'){
					this.ruleForm.tongzhineirong = obj[o];
					this.ro.tongzhineirong = true;
					continue;
					}
					if(o=='tongzhileixing'){
					this.ruleForm.tongzhileixing = obj[o];
					this.ro.tongzhileixing = true;
					continue;
					}
					if(o=='tongzhishijian'){
					this.ruleForm.tongzhishijian = obj[o];
					this.ro.tongzhishijian = true;
					continue;
					}
					if(o=='yanyuanzhanghao'){
					this.ruleForm.yanyuanzhanghao = obj[o];
					this.ro.yanyuanzhanghao = true;
					continue;
					}
					if(o=='nicheng'){
					this.ruleForm.nicheng = obj[o];
					this.ro.nicheng = true;
					continue;
					}
					if(o=='lianxidianhua'){
					this.ruleForm.lianxidianhua = obj[o];
					this.ro.lianxidianhua = true;
					continue;
					}
					if(o=='shangjiazhanghao'){
					this.ruleForm.shangjiazhanghao = obj[o];
					this.ro.shangjiazhanghao = true;
					continue;
					}
					if(o=='shangjianicheng'){
					this.ruleForm.shangjianicheng = obj[o];
					this.ro.shangjianicheng = true;
					continue;
					}
					if(o=='lianxifangshi'){
					this.ruleForm.lianxifangshi = obj[o];
					this.ro.lianxifangshi = true;
					continue;
					}
					if(o=='shangjiazhaopian'){
					this.ruleForm.shangjiazhaopian = obj[o].split(",")[0];
					this.ro.shangjiazhaopian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
            tongzhineirongChange(e) {
                this.ruleForm.tongzhineirong = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async yanyuanzhanghaoChange (e) {
				this.yanyuanzhanghaoIndex = e.target.value
				this.ruleForm.yanyuanzhanghao = this.yanyuanzhanghaoOptions[this.yanyuanzhanghaoIndex]
				let res = await this.$api.follow(`xiaoyanyuan`, `yanyuanzhanghao`,{
					columnValue: this.ruleForm.yanyuanzhanghao
				});
				if(res.data.nicheng){
					this.ruleForm.nicheng = res.data.nicheng
				}
				if(res.data.lianxidianhua){
					this.ruleForm.lianxidianhua = res.data.lianxidianhua
				}
			},

			// 多级联动参数


			// 日长控件选择日期时间
			tongzhishijianConfirm(val) {
				console.log(val)
				this.ruleForm.tongzhishijian = val.result;
				this.$forceUpdate();
			},

			// 下拉变化
			tongzhileixingChange(e) {
				this.tongzhileixingIndex = e.target.value
				this.ruleForm.tongzhileixing = this.tongzhileixingOptions[this.tongzhileixingIndex]
			},

			shangjiazhaopianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.shangjiazhaopian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {























//跨表计算判断
				var obj;
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`xiaoxi`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`xiaoxi`, this.ruleForm);
						}else{
							await this.$api.add(`xiaoxi`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`xiaoxi`, this.ruleForm);
					}else{
						await this.$api.add(`xiaoxi`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
