<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				qianbaobianhao: '',
				yanyuanzhanghao: '',
				nicheng: '',
				yongjin: '',
				beizhu: '',
				fukuanshijian: '',
				shangjiazhanghao: '',
				shangjianicheng: '',
				lianxifangshi: '',
				shangjiazhaopian: '',
				},
				qianbaobianhaoOptions: [],
				qianbaobianhaoIndex: 0,
				// 登陆用户信息
				user: {},
                ro:{
                   qianbaobianhao : false,
                   yanyuanzhanghao : false,
                   nicheng : false,
                   yongjin : false,
                   beizhu : false,
                   fukuanshijian : false,
                   shangjiazhanghao : false,
                   shangjianicheng : false,
                   lianxifangshi : false,
                   shangjiazhaopian : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.fukuanshijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.shangjiazhanghao = this.user.shangjiazhanghao
			this.ro.shangjiazhanghao = true;
			this.ruleForm.shangjianicheng = this.user.shangjianicheng
			this.ro.shangjianicheng = true;
			this.ruleForm.lianxifangshi = this.user.lianxifangshi
			this.ro.lianxifangshi = true;


			// 下2
			res = await this.$api.option(`qianbao`,`qianbaobianhao`,{});
			this.qianbaobianhaoOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zhifuyongjin`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='qianbaobianhao'){
					this.ruleForm.qianbaobianhao = obj[o];
					this.ro.qianbaobianhao = true;
					continue;
					}
					if(o=='yanyuanzhanghao'){
					this.ruleForm.yanyuanzhanghao = obj[o];
					this.ro.yanyuanzhanghao = true;
					continue;
					}
					if(o=='nicheng'){
					this.ruleForm.nicheng = obj[o];
					this.ro.nicheng = true;
					continue;
					}
					if(o=='yongjin'){
					this.ruleForm.yongjin = obj[o];
					this.ro.yongjin = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
					if(o=='fukuanshijian'){
					this.ruleForm.fukuanshijian = obj[o];
					this.ro.fukuanshijian = true;
					continue;
					}
					if(o=='shangjiazhanghao'){
					this.ruleForm.shangjiazhanghao = obj[o];
					this.ro.shangjiazhanghao = true;
					continue;
					}
					if(o=='shangjianicheng'){
					this.ruleForm.shangjianicheng = obj[o];
					this.ro.shangjianicheng = true;
					continue;
					}
					if(o=='lianxifangshi'){
					this.ruleForm.lianxifangshi = obj[o];
					this.ro.lianxifangshi = true;
					continue;
					}
					if(o=='shangjiazhaopian'){
					this.ruleForm.shangjiazhaopian = obj[o].split(",")[0];
					this.ro.shangjiazhaopian = true;
					continue;
					}
				}
            this.ruleForm.yongjin = 0;
            this.ro.yongjin = false;
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async qianbaobianhaoChange (e) {
				this.qianbaobianhaoIndex = e.target.value
				this.ruleForm.qianbaobianhao = this.qianbaobianhaoOptions[this.qianbaobianhaoIndex]
				let res = await this.$api.follow(`qianbao`, `qianbaobianhao`,{
					columnValue: this.ruleForm.qianbaobianhao
				});
				if(res.data.yanyuanzhanghao){
					this.ruleForm.yanyuanzhanghao = res.data.yanyuanzhanghao
				}
				if(res.data.nicheng){
					this.ruleForm.nicheng = res.data.nicheng
				}
			},

			// 多级联动参数


			// 日长控件选择日期时间
			fukuanshijianConfirm(val) {
				console.log(val)
				this.ruleForm.fukuanshijian = val.result;
				this.$forceUpdate();
			},


			shangjiazhaopianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.shangjiazhaopian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {





















//跨表计算判断
				var obj;
				if(this.ruleForm.yongjin&&(!this.$validate.isIntNumer(this.ruleForm.yongjin))){
					this.$utils.msg(`佣金应输入整数`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`zhifuyongjin`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
                        if(!obj) {
                            obj = uni.getStorageSync('crossObj');
                        }
                        var table = uni.getStorageSync('crossTable');
                        obj.yongjin = parseFloat(obj.yongjin) + parseFloat(this.ruleForm.yongjin)
                        await this.$api.update(`${table}`, obj);
						if(this.ruleForm.id){
							await this.$api.update(`zhifuyongjin`, this.ruleForm);
						}else{
							await this.$api.add(`zhifuyongjin`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
                    if(!obj) {
                        obj = uni.getStorageSync('crossObj');
                    }
                    var table = uni.getStorageSync('crossTable');
                    obj.yongjin = parseFloat(obj.yongjin) + parseFloat(this.ruleForm.yongjin)
                    await this.$api.update(`${table}`, obj);
					if(this.ruleForm.id){
						await this.$api.update(`zhifuyongjin`, this.ruleForm);
					}else{
						await this.$api.add(`zhifuyongjin`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
